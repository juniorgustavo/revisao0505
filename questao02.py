#Pedir 2 números para o usuário
#Imprimir a soma, diferença, produto e divisão.

a = float(input("Informe o primeiro número: "))
b = float(input("Informe o segundo número: "))

soma = a + b
diferenca = a - b
produto = a * b
divisao = a / b


print(f"A soma dos dois números é {soma} \n "
      f"A diferença dos dois números é {diferenca} \n "
      f"O produto dos dois números é {produto} \n "
      f"A divisão entre os dois números é {divisao}")